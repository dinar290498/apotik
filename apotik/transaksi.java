    float total=0;
    float disc=0;
    float grandTotal=0;
    DecimalFormat dfo = new DecimalFormat("#,##0");
    private void allTransaksi(){
        
        int n=0;
        total=0;
        try{
            while(n<tmTemp.fieldTemp.size()){
                total=total+tmTemp.fieldTemp.get(n).getSubTotal();
                n++;
            }

            disc=Integer.parseInt(jSpinner3.getValue().toString());
           
            float potongan=disc/100*total;
            grandTotal=total-potongan;
            
            jTextField3.setText("Rp. "+dfo.format(total));
            jTextField5.setText("Rp. "+dfo.format(potongan));
            jTextField6.setText("Rp. "+dfo.format(grandTotal));
        }
        catch(Exception z){
            jTextField3.setText("Rp. 0");
            jTextField5.setText("Rp. 0");
            jTextField6.setText("Rp. 0");
        }
    }

    private void reset(){
        this.tmTemp.removeAll();
        allTransaksi();
        this.jComboBox1.setSelectedIndex(0);

        this.jComboBox2.setSelectedIndex(0);
        this.jSpinner1.setEnabled(false);

        setNota();
        this.jTextField8.setText("");
        this.jTextField9.setText("");
        this.jDateChooser1.setDate(new Date());
        this.jTextField2.setText("");
        jSpinner2.setValue(1);
        jSpinner3.setValue(0);
    }

            
