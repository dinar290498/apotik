    private void jTextField8KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField8KeyPressed
        int keyCode = evt.getKeyCode();
        int x = 13; //0 --> true 2 --> false
        
        
        if (keyCode == evt.VK_ENTER){
            if(nominal>=grandTotal && tmTemp.fieldTemp.size()>0){
                x=JOptionPane.showConfirmDialog(rootPane, "Simpan Transaksi ini ????","Konfirmasi ",JOptionPane.OK_CANCEL_OPTION);
                if(x==0){

                    dtPenjualan dtp=new dtPenjualan();
                    dtp.setNoNota(jTextField7.getText());
                    dtp.setIdKonsumen(jTextField10.getText());
                    dtp.setTotal(total);
                    dtp.setDiskon((int) disc);
                    dtp.setGrandTotal(grandTotal);
                    dtp.setTglTransaksi(jDateChooser1.getDate());
                    if(jComboBox2.getSelectedIndex()==0){
                        dtp.setKet(1);
                        dtp.setTglBayar(jDateChooser1.getDate());
                    }
                    else {
                        dtp.setKet(2);
                        
                    }
                    if (dbPenjualan.insert(dtp)) {
                        tmp.insert(dtp);
                        int n = 0;
                        while (n < tmTemp.fieldTemp.size()) {
                          dtPenjualanDet dtpd = new dtPenjualanDet();
                          dtpd.setNoNota(this.jTextField7.getText());
                          dtpd.setKodeObat(((dtPenjualanDet)tmTemp.fieldTemp.get(n)).getKodeObat());
                          dtpd.setNamaObat(((dtPenjualanDet)tmTemp.fieldTemp.get(n)).getNamaObat());
                          dtpd.setHarga(((dtPenjualanDet)tmTemp.fieldTemp.get(n)).getHarga());
                          dtpd.setBny(((dtPenjualanDet)tmTemp.fieldTemp.get(n)).getBny());
                          dtpd.setTotal(((dtPenjualanDet)tmTemp.fieldTemp.get(n)).getTotal());
                          dtpd.setDisc(((dtPenjualanDet)tmTemp.fieldTemp.get(n)).getDisc());
                          dtpd.setSubTotal(((dtPenjualanDet)tmTemp.fieldTemp.get(n)).getSubTotal());

                          if (this.dbPenjualanDet.insert(dtpd))
                          {
                            if (this.dbObat.editObat2(((dtPenjualanDet)tmTemp.fieldTemp.get(n)).getKodeObat(), ((dtPenjualanDet)tmTemp.fieldTemp.get(n)).getBny())) {
                              this.tmTableObat.updateStok(this.tmTableObat.search(((dtPenjualanDet)tmTemp.fieldTemp.get(n)).getKodeObat()), ((dtPenjualanDet)tmTemp.fieldTemp.get(n)).getBny(), true);
                            }
                          }

                          n++;
                        }               
                    }
                    reset();
                }
            }
            else if(tmTemp.fieldTemp.size()==0)
                JOptionPane.showMessageDialog(rootPane, "Isi Transaksi Terlebih Dahulu???","ERROR",JOptionPane.ERROR_MESSAGE);
            else if(nominal==0)
                JOptionPane.showMessageDialog(rootPane, "Masukan uang Konsumen","ERROR",JOptionPane.ERROR_MESSAGE);
            else
                JOptionPane.showMessageDialog(rootPane, "Pembayaran Tidak Mencukupi","ERROR",JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jTextField8KeyPressed

