    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int x=tmTableObat.search(jTextField2.getText());
        int y=tmTemp.search(jTextField2.getText());

        if(x!=-1 && y==-1){
            int bny=Integer.parseInt(jSpinner2.getValue().toString());

            if(tmTableObat.persediaan(jTextField2.getText(),bny)==-1){

                dtPenjualanDet dtpd=new dtPenjualanDet();
                dtpd.setKodeObat(tmTableObat.fieldObat.get(x).getKodeObat());
                dtpd.setNamaObat(tmTableObat.fieldObat.get(x).getNamaObat());

                float harga=0;

                if(jComboBox1.getSelectedIndex()==0)
                    harga=(float) tmTableObat.fieldObat.get(x).gethUmum();
                else if(jComboBox1.getSelectedIndex()==1)
                    harga=(float) tmTableObat.fieldObat.get(x).gethPelanggan();
                else if(jComboBox1.getSelectedIndex()==2)
                    harga=(float) tmTableObat.fieldObat.get(x).gethResep();

                dtpd.setHarga(harga);

                dtpd.setBny(bny);
                float total=harga*bny;
                dtpd.setTotal(total);
                int disc=0;
                dtpd.setDisc(disc);
                float subTotal=total-((disc/100)*total);
                dtpd.setSubTotal(subTotal);

                tmTemp.insert(dtpd);

                allTransaksi();
            }
            else
                JOptionPane.showMessageDialog(rootPane, "Stok Barang Tidak Mencukupi","ERROT",JOptionPane.ERROR_MESSAGE);
        }
        else if(x==-1)
            JOptionPane.showMessageDialog(rootPane, "Data Obat Tidak Di Temukan","ERROT",JOptionPane.ERROR_MESSAGE);
        else if(y!=-1)
            JOptionPane.showMessageDialog(rootPane, "Data Obat Sudah Dalam Daftar","ERROT",JOptionPane.ERROR_MESSAGE);
        this.jDateChooser1.setDate(new Date());
    }//GEN-LAST:event_jButton2ActionPerformed

