    private void botton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botton1ActionPerformed
        if(!jTextField1.getText().trim().equals("") && !jTextField2.getText().trim().equals("") ){
            dt=new dtObat();
            dt.setKodeObat(jTextField1.getText());
            dt.setNamaObat(jTextField2.getText());
            dt.setStok(Integer.parseInt(jTextField3.getText()));
            dt.sethBeli(Float.parseFloat(jTextField5.getText()));
            dt.sethUmum(Float.parseFloat(jTextField4.getText()));
            dt.sethPelanggan(Float.parseFloat(jTextField6.getText()));
            dt.sethResep(Float.parseFloat(jTextField7.getText()));
            if(jComboBox6.getSelectedIndex()!=0)
                dt.setSatuan(jComboBox6.getSelectedItem().toString());
            else dt.setSatuan("");

            if(jComboBox1.getSelectedIndex()!=0)
                dt.setJenisObat(jComboBox1.getSelectedItem().toString());
            else dt.setJenisObat("");

            if(jComboBox2.getSelectedIndex()!=0)
                dt.setKatagori(jComboBox2.getSelectedItem().toString());
            else dt.setKatagori("");
            
            if(jComboBox4.getSelectedIndex()!=0)
                dt.setProduk(jComboBox4.getSelectedItem().toString());
            else dt.setProduk("");

            if(jComboBox5.getSelectedIndex()!=0)
                dt.setDistributor(jComboBox5.getSelectedItem().toString());
            else dt.setDistributor("");
            
            if(jDateChooser1.getDate()!=null)
                dt.setKadaluarsa(jDateChooser1.getDate());
            else dt.setKadaluarsa(null);

                setCek(true);
                setVisible(false);
          
        }
        else
            JOptionPane.showMessageDialog(rootPane, "Mohon Lengkapi Data Obat !!!","ERROT",JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_botton1ActionPerformed

