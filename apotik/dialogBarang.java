    DecimalFormat dfo = new DecimalFormat("###0");
    public void transper(dtObat dto){
        jTextField1.setText(dto.getKodeObat());
        jTextField2.setText(dto.getNamaObat());
        jTextField3.setText(String.valueOf(dto.getStok()));
        jTextField5.setText(dfo.format(dto.gethBeli()));
        jTextField4.setText(dfo.format(dto.gethUmum()));
        jTextField6.setText(dfo.format(dto.gethPelanggan()));
        jTextField7.setText(dfo.format(dto.gethResep()));

        try        {
          if ((dto.getSatuan().equals("")) || (dto.getSatuan().equals(" ")))
            jComboBox6.setSelectedIndex(0);
          else jComboBox6.setSelectedItem(dto.getSatuan().trim());
        }
        catch (Exception z){
          this.jComboBox6.setSelectedIndex(0);
        }

        try{
          if (dto.getJenisObat().equals(" "))
            this.jComboBox1.setSelectedIndex(0);
          else this.jComboBox1.setSelectedItem(dto.getJenisObat());
        }
        catch (Exception z){
          this.jComboBox1.setSelectedIndex(0);
        }

        try{
          if (dto.getKatagori().equals(""))
            this.jComboBox2.setSelectedIndex(0);
          else this.jComboBox2.setSelectedItem(dto.getKatagori());
        }
        catch (Exception z){
          this.jComboBox2.setSelectedIndex(0);
        }

        try{
          if (dto.getProduk().equals(""))
            this.jComboBox4.setSelectedIndex(0);
          else this.jComboBox4.setSelectedItem(dto.getProduk());
        }
        catch (Exception z){
          this.jComboBox4.setSelectedIndex(0);
        }

        try{
          if (dto.getDistributor().equals(""))
            this.jComboBox5.setSelectedIndex(0);
          else this.jComboBox5.setSelectedItem(dto.getDistributor());
        }
        catch (Exception z){
          this.jComboBox5.setSelectedIndex(0);
        }

        jDateChooser1.setDate(dto.getKadaluarsa());

    }

