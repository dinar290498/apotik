    private void botton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botton5ActionPerformed
    int n = this.jTable2.getSelectedRow();
    if (n >= 0) {
      if ((!this.jTextField2.getText().trim().equals("")) && (!this.jTextField3.getText().trim().equals(""))) {
        dtPelanggan dtp = new dtPelanggan();
        dtp.setKodePelanggan(this.jTextField2.getText());
        dtp.setNamaPelanggan(this.jTextField3.getText());
        dtp.setAlamat(this.jTextPane1.getText());
        dtp.setTelp1(this.jTextField4.getText());
        dtp.setTelp2(this.jTextField5.getText());
        dtp.setIdIdentitas(this.jComboBox2.getSelectedItem().toString());
        dtp.setNoIdentitas(this.jTextField6.getText());
        dtp.setSaldoPiutang(Integer.parseInt(this.jTextField7.getText()));

        if (this.dbPelanggan.edit(dtp, ((dtPelanggan)tmPelanggan.fieldPelanggan.get(n)).getKodePelanggan())) {
          this.jTable2.setRowSorter(null);
          this.sorterPelanggan.setModel(null);
          this.tmPelanggan.update(dtp, n);
        }

      }
      else
      {
        JOptionPane.showMessageDialog(this.rootPane, "Perubahan data tidak Valid", "ERROT", 0);
      }
    }
    else JOptionPane.showMessageDialog(this.rootPane, "Pilih data yang akan di rubah", "ERROT", 0);
    reload();
}//GEN-LAST:event_botton5ActionPerformed

