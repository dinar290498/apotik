    class TabKeField implements ListSelectionListener{

        public void valueChanged(ListSelectionEvent e) {
            if(e.getValueIsAdjusting()) return ;
            ListSelectionModel lsm=(ListSelectionModel)e.getSource();
            int n=lsm.getMinSelectionIndex();
            try{
                jTextField9.setText(tmPenjualan.fieldPenjualan.get(n).getNoNota());
                jTextField10.setText(tmPenjualan.fieldPenjualan.get(n).getIdKonsumen());
                jTextField11.setText(tmPelanggan.cariNama(jTextField10.getText())); //nama
                jTextField12.setText("Rp. "+dfo.format(tmPenjualan.fieldPenjualan.get(n).getTotal()));
                jTextField13.setText(String.valueOf(tmPenjualan.fieldPenjualan.get(n).getDiskon()));
                jTextField14.setText("Rp. "+dfo.format(tmPenjualan.fieldPenjualan.get(n).getGrandTotal()));
                jDateChooser1.setDate(tmPenjualan.fieldPenjualan.get(n).getTglTransaksi());
                if(tmPenjualan.fieldPenjualan.get(n).getKet()==1){
                    jComboBox4.setSelectedIndex(0);
                }
                else {
                    jComboBox4.setSelectedIndex(1);
                }

                jDateChooser2.setDate(tmPenjualan.fieldPenjualan.get(n).getTglBayar());

                try{
                      tmDetTemp.removeAllData();
                      ResultSet set = koneksiAcces.stat.executeQuery("select * from penjualanDet where noNota='" + jTable3.getValueAt(n, 0).toString() + "'");
                      String kodeObat = "";
                      while (set.next()) {
                        dtPenjualanDet da = new dtPenjualanDet();
                        da.setNoNota(set.getString("noNota"));
                        kodeObat = set.getString("kodeObat");
                        da.setKodeObat(kodeObat);
                        da.setNamaObat(formUtama.this.tmTableObatZ.criNama(kodeObat));
                        da.setHarga(set.getFloat("harga"));
                        da.setBny(set.getInt("bny"));
                        da.setSubTotal(set.getFloat("subTotal"));
                        da.setDisc(set.getInt("disc"));
                        da.setTotal(set.getFloat("total"));

                        formUtama.this.tmDetTemp.insert(da);
                      }

                      set.close();
                }
                catch (Exception z) {
                  System.out.print(z.getMessage());
                }

            }
            catch(Exception z){
                jTextField9.setText("");
                jTextField10.setText("");
                jTextField11.setText("");
                jTextField12.setText("");
                jTextField13.setText("");
                jTextField14.setText("");
                jDateChooser1.setDate(null);
                jComboBox4.setSelectedIndex(0);
                jDateChooser2.setDate(null);
                formUtama.this.tmDetTemp.removeAllData();
            }

        }

    }

